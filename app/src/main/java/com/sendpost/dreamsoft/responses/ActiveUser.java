package com.sendpost.dreamsoft.responses;

public class ActiveUser {
    String id;
    String supporter_id;
    String user_id;
    String pin;
    String amount;
    String remark;
    String remarkmobile;
    String created_at;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSupporter_id() {
        return supporter_id;
    }

    public void setSupporter_id(String supporter_id) {
        this.supporter_id = supporter_id;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getPin() {
        return pin;
    }

    public void setPin(String pin) {
        this.pin = pin;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public String getRemarkmobile() {
        return remarkmobile;
    }

    public void setRemarkmobile(String remarkmobile) {
        this.remarkmobile = remarkmobile;
    }

    public String getCreated_at() {
        return created_at;
    }

    public void setCreated_at(String created_at) {
        this.created_at = created_at;
    }

    public String getUpdated_at() {
        return updated_at;
    }

    public void setUpdated_at(String updated_at) {
        this.updated_at = updated_at;
    }

    String updated_at;
}
